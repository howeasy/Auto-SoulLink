def main():
    print(f"Fetching {SPECIES_H_URL} ...")
    data = urllib.request.urlopen(SPECIES_H_URL).read().decode()

    rr_defines = {}
    for m in re.finditer(r"#define\s+SPECIES_(\w+)\s+0x([0-9A-Fa-f]+)", data):
        name = m.group(1)
        num = int(m.group(2), 16)
        if name in ("NONE", "EGG"):
            continue
        if num not in rr_defines:
            rr_defines[num] = name

    print(f"Parsed {len(rr_defines)} species entries")

    # Build all lookup dicts
    all_natdex = {}
    all_natdex.update(GEN3_NATDEX)
    all_natdex.update(GEN4_NATDEX)
    all_natdex.update(GEN5_NATDEX)
    all_natdex.update(GEN6_NATDEX)
    all_natdex.update(GEN7_NATDEX)
    all_natdex.update(GEN8_NATDEX)
    all_natdex.update(GEN9_NATDEX)
    all_natdex.update(FORM_BASE_MAP)

    # Map RR internal ID → NatDex
    rr_to_nat = {}
    unmapped = []
    for rr_id, define_name in sorted(rr_defines.items()):
        if rr_id <= 251:
            # Gen 1-2: identity
            continue
        if define_name in all_natdex:
            nat = all_natdex[define_name]
            if nat != rr_id:
                rr_to_nat[rr_id] = nat
        elif define_name in FORM_BASE_MAP:
            nat = FORM_BASE_MAP[define_name]
            if nat != rr_id:
                rr_to_nat[rr_id] = nat
        else:
            unmapped.append((rr_id, define_name))

    print(f"Mapped: {len(rr_to_nat)}, Unmapped: {len(unmapped)}")
    if unmapped:
        print("Unmapped species (will use RR ID as-is for sprites):")
        for rr_id, name in unmapped[:30]:
            print(f"  {rr_id} (0x{rr_id:03X}): {name}")

    # Output
    lines = []
    batch = []
    for rr_id in sorted(rr_to_nat.keys()):
        batch.append(f"{rr_id}:{rr_to_nat[rr_id]}")
        if len(batch) >= 8:
            lines.append("    " + ",".join(batch) + ",")
            batch = []
    if batch:
        lines.append("    " + ",".join(batch) + ",")

    with open("data/rr_cfru_to_national.txt", "w") as f:
        f.write("\n".join(lines))
    print(f"Wrote {len(rr_to_nat)} mappings to data/rr_cfru_to_national.txt")

    # Verify key entries
    checks = {
        1025: 37,   # Vulpix_A → Vulpix (NatDex 37)
        706: 950,    # Klawf (NatDex 950)
        936: 936,    # Kingambit at RR ID 936 → check if mapped
        747: 1003,   # Ting-Lu
    }
    for rr_id, expected_nat in checks.items():
        actual = rr_to_nat.get(rr_id, rr_id)
        ok = "✓" if actual == expected_nat else "✗"
        print(f"  {ok} RR {rr_id} → NatDex {actual} (expected {expected_nat})")


if __name__ == "__main__":
    main()
